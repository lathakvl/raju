package com.Loops;

public class NestedLoopsExample {
  public static void main(String[] args) {
	for(int a=1;a<=4;a++)
	{
		for(int b=0;b<a;b++)
		{
		System.out.print(a);
		}
         System.out.println();
	  }     
       System.out.println("***********");
      for (int a=1;a<=4;a++)
      {
         for (int b=1;b<=a;b++)
         { 	 
         System.out.print(a);
      }
      System.out.println();
      }
      System.out.println("============");
      System.out.println("1");
      for(int a=3;a<=4;a++)
      {
     for(int b=1;b<=a;b++)
      
     {
    	 System.out.println(b+"");
     }
      System.out.println();
      
      }
   System.out.println("$$$$$$$$");      
   for(int a=7;a>=5;a--)
   { 
	  for (int b=a;b>=5;b--)
	  {   
   System.out.println(b+"");
	  
	  }
	   
	   
   }	   
}
}
	   
	   
	   
	   
	   
	   
	   
	   
	   
	   
	   
	   
	   
	   
	   
	   
	   
	   
	   
	   
	   




